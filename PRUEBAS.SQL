SELECT AI.CODIGO, AI.CODIGO_ARTICULO, A.NOMBRE, A.PVP_VENTA, AI.CODIGO_IMAGEN, I.URL, AI.NUMERO_ORDEN
FROM wabaw.Articulos_IMAGENES AS AI, 
    wabaw.articulos as A, 
    wabaw.imagenes as I 
where AI.CODIGO_ARTICULO = A.CODIGO 
AND AI.CODIGO_IMAGEN = I.CODIGO 
AND AI.NUMERO_ORDEN = 1 
AND A.COD_FAMILIA = 10 
ORDER BY A.NOMBRE, A.PVP_VENTA ASC;

SELECT A.CODIGO_ARTICULO, A.NOMBRE, AI.CODIGO, AI.CODIGO_ARTICULO, AI.CODIGO_IMAGEN, AI.NUMERO_ORDEN, I.URL
FROM wabaw.articulos as A
left OUTER JOIN WABAW.ARTICULOS_IMAGENES AI
ON (AI.CODIGO_ARTICULO = A.CODIGO )
left OUTER JOIN WABAW.IMAGENES I
ON (AI.CODIGO_IMAGEN = A.CODIGO and ai.numero_orden = 1
)
where  A.COD_FAMILIA = 10 
ORDER BY A.NOMBRE, A.PVP_VENTA ASC;



